package cva.pc.demeter.utilidades;

public class Entero  {
	int valor;

	public Entero(int valor) {
		super();
		this.valor = valor;
	}
	
	public Entero() {
		super();
		this.valor = 0;
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}
	

}
